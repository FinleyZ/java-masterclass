package academy.learningJava;

public class Person {
    private boolean cat;
    private boolean yiLing;

    public Person(boolean cat, boolean yiLing) {
        this.cat = cat;
        this.yiLing = yiLing;
    }

    public boolean isCat() {
        return cat;
    }

    public boolean isYiLing() {
        return yiLing;
    }
}
